class Solution(object):
    def searchMatrix(self, matrix, target):
        """
        :type matrix: List[List[int]]
        :type target: int
        :rtype: bool
        """
        x = len(matrix)
        if x > 0:
            y = len(matrix[0])
            for i in range(x):
                if matrix[i][-1] < target:
                    continue
                for j in range(y):
                    if matrix[i][j] > target:
                        if j == 0:
                            return False
                        y = j
                        break
                    elif matrix[i][j] == target:
                        return True
        return False


